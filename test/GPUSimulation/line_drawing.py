# Draw a line between the two points.
def draw_line(x1,y1,x2,y2,arr=screen):
    ##########################
    # initial calculations
    ##########################
    # find dx and dy for slope calculations
    if ( x2 > x1 ):
        dx = x2 - x1
        dy = y2 - y1
    elif (x1 == x2 and y1 > y2):
        dx = x2 - x1
        dy = y2 - y1
    else:
        dx = x1 - x2
        dy = y1 - y2
    # choose beginning x an y values.
    y = y1 if y1 < y2 else y2
    yf = y1 if y1 > y2 else y2
    x = x1 if x1 < x2 else x2
    xf = x1 if x1 > x2 else x2
    eps = 0
    positive_slope =  (dx > 0 and dy > 0) or (dx < 0 and dy < 0)

    # slope detector
    # check for positive or negative slope.
    if (positive_slope): # positive
        if (dx >= dy):
            smallslope = 1
        else:
            smallslope = 0
    else:                # negative
        if (abs(dx) >= abs(dy)):
            y = y1 if y1 > y2 else y2
            yf = y1 if y1 < y2 else y2
            smallslope = 1
        else:
            x = x1 if x1 > x2 else x2
            xf = x1 if x1 < x2 else x2
            smallslope = 0
        
    #print "dx =",dx, "dy =", dy,"xi =",x,"yi =",y,"xf =",xf,"yf =",yf
    #print "slope positive =",positive_slope,"small slope =",smallslope

    ################################
    # DRAW POSITIVE SLOPES
    ################################
    if positive_slope:
        if smallslope:
            # draw each pixel for positive slopes from 0 to 1
            for x in range(x,xf+1):
                if (x >= 0 and y >= 0 and x < WIDTH and y < HEIGHT):
                    arr[y][x] = '|'
                eps += dy
                if (eps << 1) >= dx:
                    y += 1
                    eps -= dx
        else:
            # draw each pixel for positive slopes from 1 to inf
            for y in range(y,yf+1):
                if (x >= 0 and y >= 0 and x < WIDTH and y < HEIGHT):
                    arr[y][x] = '|'
                eps += dx
                if (eps << 1) >= dy:
                    x += 1
                    eps -= dy
    ################################
    # DRAW NEGATIVE SLOPES
    ################################
    else:
    # draw each pixel for negative slopes from 0 to 1
        if smallslope:
            for x in range(x,xf+1):
                if (x >= 0 and y >= 0 and x < WIDTH and y < HEIGHT):
                    arr[y][x] = '|'
                eps += dy
                if (eps << 1) <= -dx:
                    y -= 1
                    eps += dx
    # draw each pixel for negative slopes from 1 to inf
        else:
            dx = -dx
            dy = -dy
            # draw each pixel for positive slopes from 1 to inf
            for y in range(y,yf+1):
                #print "x=",x,"y=",y
                if (x >= 0 and y >= 0 and x < WIDTH and y < HEIGHT):
                    arr[y][x] = '|'
                eps += dx
                if (eps << 1) <= -dy:
                    x -= 1
                    eps += dy

